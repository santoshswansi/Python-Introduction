import os

os.chdir(r'C:\Users\santo\PycharmProjects\Parse File Names\Sample Folder')

for file in os.listdir():

    count, f_name, m_name, l_name = file.split('-')
    count = count.zfill(2)
    extension = '.txt'

    new_name = f'{count}-{f_name} {m_name} {l_name}{extension}'

    os.rename(file, new_name)
